#include "fillit.h"

char	*ft_clear(char *tab)
{
	int		i;
	int		p;
	int		h;

	i = 0;
	while (tab[i])
	{ 
		p = 0;
		while (tab[i] == ".")
		{	
			if (i % 4 == 0 && p == 4)
				break;
			i++;
			p++;
		}

		if (tab[i] == "#")
			h++;
		i++;
	}
}
