#include "fillit.h"

int		ft_check_tetri_diag(char *tab)
{
	int		i;
	int		j;

	i = 0;
	while (tab[i])
	{
		while (tab[i] == '#')
		{
			j = 0;
			while (tab[i + 5] && j <= 6)
			{
				j++;
				i++;
				if (tab[i] == '#')
					break;
			}
			i++;
		}
	}
	return (0);
}
